num1: int = int(input("what is your first number? "))
num2: int = int(input("what is your second number? "))
num3: int = int(input("what is your third number? "))
sum_ = num1 + num2 + num3
mul_ = num1 * num2 * num3

print(num1, "+", num2, "+", num3, "=", sum_)
print(num1, "*", num2, "*", num3, "=", mul_)
